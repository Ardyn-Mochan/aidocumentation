import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are an AI documentation assistant for AI Cloud, a comprehensive platform for building AI-powered applications.

Your role is to help users navigate the documentation, answer questions about:
- Getting started with AI Cloud
- API Reference (authentication, completions, embeddings, fine-tuning, assistants)
- Data & Storage (datasets, vector stores, file management)
- Compute resources (GPU instances, scaling, batch processing)
- Integrations (SDKs, webhooks, third-party)
- Security (API keys, permissions, compliance)
- Configuration (environment, rate limits, billing)

Guidelines:
- Be concise and helpful
- Provide code examples when relevant
- Reference specific documentation sections
- If you don't know something, direct users to the appropriate docs section
- Use markdown formatting for code blocks and lists

Available Models: ai-cloud-4, ai-cloud-4-mini, ai-cloud-vision, ai-cloud-embed

Example API usage:
\`\`\`typescript
import { AICloud } from '@ai-cloud/sdk';
const client = new AICloud({ apiKey: process.env.AI_CLOUD_API_KEY });
const response = await client.chat.completions.create({
  model: 'ai-cloud-4',
  messages: [{ role: 'user', content: 'Hello!' }],
});
\`\`\``;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required, please add funds." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("Chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
